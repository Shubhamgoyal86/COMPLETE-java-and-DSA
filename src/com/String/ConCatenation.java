package com.String;

public class ConCatenation {
    public static void main(String[] args) {
        String s1 ="Shubham";
        String s2 = "Goyal";
        String s3 = String.join(" ",s1,s2);
        System.out.println(s3);
        System.out.println(s3.toString());
        String s="hello";
        System.out.println(s.substring(0,4));
    }
}
